package model;

public class AdminReport {
	private String imeprez;
	private Integer brPrijatelja;
	private Integer brPoruka;
	private Integer brPostova;

	public AdminReport() {
		
	}
	
	public String getImeprez() {
		return imeprez;
	}
	public void setImeprez(String imeprez) {
		this.imeprez = imeprez;
	}
	public Integer getBrPrijatelja() {
		return brPrijatelja;
	}
	public void setBrPrijatelja(Integer brPrijatelja) {
		this.brPrijatelja = brPrijatelja;
	}
	public Integer getBrPoruka() {
		return brPoruka;
	}
	public void setBrPoruka(Integer brPoruka) {
		this.brPoruka = brPoruka;
	}
	public Integer getBrPostova() {
		return brPostova;
	}
	public void setBrPostova(Integer brPostova) {
		this.brPostova = brPostova;
	}
	
	
}
