package model;

public class KorisnikReport {
	private String imeprez;
	private Integer zajPrij;
	private Integer zajStr;
	private Integer zajGrup;
	private Integer primPor;
	private Integer poslPor;
	
	public KorisnikReport() {
		
	}
	
	public String getImeprez() {
		return imeprez;
	}
	public void setImeprez(String imeprez) {
		this.imeprez = imeprez;
	}
	public Integer getZajPrij() {
		return zajPrij;
	}
	public void setZajPrij(Integer zajPrij) {
		this.zajPrij = zajPrij;
	}
	public Integer getZajStr() {
		return zajStr;
	}
	public void setZajStr(Integer zajStr) {
		this.zajStr = zajStr;
	}
	public Integer getZajGrup() {
		return zajGrup;
	}
	public void setZajGrup(Integer zajGrup) {
		this.zajGrup = zajGrup;
	}
	public Integer getPrimPor() {
		return primPor;
	}
	public void setPrimPor(Integer primPor) {
		this.primPor = primPor;
	}
	public Integer getPoslPor() {
		return poslPor;
	}
	public void setPoslPor(Integer poslPor) {
		this.poslPor = poslPor;
	}
}
